public class Elseif {
    public static void main(String[] args) {
        int marks=95;

        if(marks<50){
            System.out.println("fail");

        }

        else if(marks >=50  && marks< 60){
            System.out.println("C");
        }
        else if(marks >=60  && marks< 100){
            System.out.println("B");
        }
    }

}
